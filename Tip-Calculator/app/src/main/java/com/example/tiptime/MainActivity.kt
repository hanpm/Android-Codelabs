// Place MutableState here
var amountInput: MutableState<String> = mutableStateOf("0")

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        enableEdgeToEdge()
        super.onCreate(savedInstanceState)
        setContent {
            TipTimeTheme {
                Surface(
                    modifier = Modifier.fillMaxSize(),
                ) {
                    TipTimeLayout()
                }
            }
        }
    }
}

//You hoisted the state to TipTimeLayout(), now pass it to EditNumberField().
// In the TipTimeLayout() function,
// update the EditNumberField() function call to use the hoisted state
@Composable
fun TipTimeLayout() {

    // Val used for variables that never change
    // var is used for changing variables

    //By importing getters and setters, we don't need to access values v.i.a .value property
    var amountInput by remember {mutableStateOf("")}
    // Must use recomposition to change the amountInput value

    val amount = amountInput.toDoubleOrNull() ?: 0.0
    // use Elvis operator to set a value if null
    val tip = calculateTip(amount)
    Column(
        modifier = Modifier
            .statusBarsPadding()
            .padding(horizontal = 40.dp)
            .safeDrawingPadding(),
        horizontalAlignment = Alignment.CenterHorizontally,
        verticalArrangement = Arrangement.Center
    ) {
        Text(
            text = stringResource(R.string.calculate_tip),
            modifier = Modifier
                .padding(bottom = 16.dp, top = 40.dp)
                .align(alignment = Alignment.Start)
        )
        EditNumberField(
            value = amountInput,
            onValueChange = {amountInput = it},
            modifier = Modifier
                .padding(bottom = 32.dp)
                .fillMaxWidth()
        )
        Text(
            text = stringResource(R.string.tip_amount, tip),
            style = MaterialTheme.typography.displaySmall
        )
        Spacer(modifier = Modifier.height(150.dp))
    }
}


// Applied the state hoisting pattern
// A value: T parameter, which is the current value to display.
//An onValueChange: (T) -> Unit â€“ callback lambda, which is triggered when the value changes so that the state can be updated elsewhere
@Composable
fun EditNumberField(
    value: String,
    onValueChange: (String) -> Unit,
    modifier: Modifier = Modifier
) {
    TextField(
        value = value,
        label = {Text(stringResource(id = R.string.bill_amount))},
        singleLine = true,
        keyboardOptions = KeyboardOptions(keyboardType = KeyboardType.Number),
        onValueChange = onValueChange,
        modifier = modifier
    )
}

/**
 * Calculates the tip based on the user input and format the tip amount
 * according to the local currency.
 * Example would be "$10.00".
 */
private fun calculateTip(amount: Double, tipPercent: Double = 15.0): String {
    val tip = tipPercent / 100 * amount
    return NumberFormat.getCurrencyInstance().format(tip)
}

